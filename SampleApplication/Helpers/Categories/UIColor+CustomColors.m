#import "UIColor+CustomColors.h"

@implementation UIColor (CustomColors)

#pragma mark - Public methods
+ (UIColor *)mediumBlueColor
{
    return [UIColor colorWithRed:45/255.0 green:106/255.0 blue:171/255.0 alpha:1.0];
}

+ (UIColor *)mediumGreenColor
{
    return [UIColor colorWithRed:29/255.0 green:107/255.0 blue:38/255.0 alpha:1.0];
}

@end
